import streamlit as st

# Заголовок страницы
st.title("Summary Page")

# Описание
st.write("Введите ссылку на YouTube видео, выберите тип саммари и нажмите кнопку для получения результата.")

# Поле ввода для ссылки на YouTube
youtube_link = st.text_input("Ссылка на YouTube видео:")

# Радио-баттоны для выбора типа саммари
summary_type = st.radio(
    "Выберите тип саммари:",
    ["Анализ данных", "Отчет по продажам", "Прогнозирование", "Обзор рынка", "План действий"]
)

# Словарь с запросами для каждого типа саммари
prompts = {
    "Анализ данных": "Пожалуйста, предоставьте анализ данных по следующему видео:",
    "Отчет по продажам": "Сформируйте отчет по продажам по следующему видео:",
    "Прогнозирование": "Прогнозируйте тренды на основе следующего видео:",
    "Обзор рынка": "Подготовьте обзор рынка по следующему видео:",
    "План действий": "Создайте план действий для следующего видео:"
}

# Отображение запроса для выбранного типа саммари
prompt_text = prompts[summary_type]
st.text_area("Запрос:", f"{prompt_text} {youtube_link}", height=100)

# Кнопка для начала обработки
if st.button("Получить Саммари"):
    if youtube_link:
        # Здесь добавьте логику для обработки ссылки и получения саммари
        st.write("Обработка видео. Пожалуйста, подождите...")
    else:
        st.error("Пожалуйста, введите ссылку на YouTube видео.")
